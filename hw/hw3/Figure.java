public class Figure {
	public static void main(String[] args) {
		printSlash();
		printText();
		printBackSlash();
		printText();
		printBackSlash();
	}

	public static void printSlash() {
		System.out.println("//////////////////////");
	}

	public static void printText() {
		System.out.println("|| Victory is mine! ||");
	}

	public static void printBackSlash() {
		System.out.println("\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\");
	}
}
